    /// <summary>Parses 7 rows of 5 characters ('#' = lit) into a [column, row] bool grid.</summary>
    private static bool[,] Parse(string row0, string row1, string row2, string row3, string row4, string row5, string row6)
    {
        var rows = new[] { row0, row1, row2, row3, row4, row5, row6 };
        var glyph = new bool[GlyphColumns, GlyphRows];

        for (var y = 0; y < GlyphRows; y++)
        {
            for (var x = 0; x < GlyphColumns; x++)
            {
                glyph[x, y] = rows[y][x] == '#';
            }
        }

        return glyph;
    }
